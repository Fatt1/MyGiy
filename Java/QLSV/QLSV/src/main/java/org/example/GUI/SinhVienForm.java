    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        try{
            SinhVien sv = new SinhVien();
            sv.setMaSV(txtMaSv.getText());
            sv.setHoTen(txtHoTen.getText());
            sv.setGioiTinh(radNam.isSelected() ? 1 : 0); // Nam: 1, Nữ: 0
            sv.setNganhHoc(cboNganhHoc.getSelectedItem().toString());
            sv.setNgaySinh(dateNgaySinh.getDate());
            List<String> kyNang = new ArrayList<>();
            if(ckbBongRo.isSelected()) {
                kyNang.add("Bóng rổ");
            }
            if(ckbBongBan.isSelected())
                kyNang.add("Bóng bàn");
            if(chkBongDa.isSelected())
                kyNang.add("Bóng đá");
            sv.setKyNang(String.join(",", kyNang));
            sinhVienBUS.addSinhVien(sv);
            initTable(sinhVienBUS.getDanhSachSinhVien());
            this.btnLamMoi.doClick();
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int rowSelected= tblSinhVien.getSelectedRow();
        if(rowSelected == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn sinh viên để xóa", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int choose = JOptionPane.showConfirmDialog(this,
                "Bạn có chắc chắn muốn xóa sinh viên này không?",
                "Xác nhận xóa",
                JOptionPane.YES_NO_OPTION);
        if(choose != JOptionPane.YES_OPTION) {
            return;
        }
        String masv = tblSinhVien.getValueAt(rowSelected, 0).toString();
        sinhVienBUS.deleteSinhVien(masv);
        initTable(sinhVienBUS.getDanhSachSinhVien());
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int rowSelected = tblSinhVien.getSelectedRow();
        if(rowSelected == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn sinh viên để cập nhật", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
            String id = tblSinhVien.getValueAt(rowSelected, 0).toString();
            SinhVien sv = new SinhVien();
            sv.setMaSV(id);
            sv.setGioiTinh(radNam.isSelected() ? 1 : 0);
            sv.setHoTen(txtHoTen.getText());
            String ngangHoc = cboNganhHoc.getSelectedItem().toString();
            sv.setNganhHoc(ngangHoc);
            sv.setNgaySinh(dateNgaySinh.getDate());
            List<String> kyNang = new ArrayList<>();
            if(ckbBongRo.isSelected()) {
                kyNang.add("Bóng rổ");
            }
            if(ckbBongBan.isSelected())
                kyNang.add("Bóng bàn");
            if(chkBongDa.isSelected())
                kyNang.add("Bóng đá");
            sv.setKyNang(String.join(",", kyNang));
            sinhVienBUS.updateSinhVien(sv);
            initTable(sinhVienBUS.getDanhSachSinhVien());
            this.btnLamMoi.doClick();
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void tblSinhVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSinhVienMouseClicked
        int selectedRow = tblSinhVien.getSelectedRow();
        if(selectedRow != -1) {
            String maSv = tblSinhVien.getValueAt(selectedRow, 0).toString();
            SinhVien sv = sinhVienBUS.getSinhVienById(maSv);
            if(sv != null) {
                selectedSinhVien = sv;
                txtMaSv.setText(sv.getMaSV());
                txtHoTen.setText(sv.getHoTen());
                dateNgaySinh.setDate(sv.getNgaySinh());
                txtMaSv.setEnabled(false);
                if(sv.getGioiTinh() == 1) {
                    radNam.setSelected(true);
                } else {
                    radNu.setSelected(true);
                }
                cboNganhHoc.setSelectedItem(sv.getNganhHoc());
                chkBongDa.setSelected(false);
                ckbBongBan.setSelected(false);
                ckbBongRo.setSelected(false);
                String[] kyNang = selectedSinhVien.getKyNang().split(",");
                for(var ky : kyNang) {
                    if (ky.trim().equals("Bóng đá")) chkBongDa.setSelected(true);
                    if (ky.trim().equals("Bóng rổ")) ckbBongRo.setSelected(true);
                    if (ky.trim().equals("Bóng bàn")) ckbBongBan.setSelected(true);
                }

            }
        }
    }//GEN-LAST:event_tblSinhVienMouseClicked

