    public void initTable(List<SinhVien> dssv) {
        DefaultTableModel model = (DefaultTableModel)  tblSinhVien.getModel();
        model.setRowCount(0);
        for(var sv : dssv) {
            Object[] row = new Object[] {
                    sv.getMaSV(),
                    sv.getHoTen(),
                    sv.getGioiTinh() == 1 ? "Nam" : "Nữ",
                    new SimpleDateFormat("dd/MM/yyyy").format(sv.getNgaySinh()),
                    sv.getNganhHoc(),
                    sv.getKyNang()
            };
            model.addRow(row);
        }

    }

